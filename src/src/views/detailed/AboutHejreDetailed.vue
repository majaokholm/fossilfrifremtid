<h1>Historie</h1>
      <p>Udbygningen af Hejrefeltet er et gammelt kuldsejlet DONG-projekt, som den britiske plastikproducent INEOS prøver at vække til live igen. 
Efter at have brugt en masse ressourcer på at bygge det bærende fundament og en olieterminal i Esbjerg, lagde DONG i 2016 deres planer om Hejrefeltet i graven. 
Ifølge Zetland skyldtes projektets kollaps faldende oliepriser og en konflikt med underleverandørerne, som gjorde investeringen for usikker. 
Den bærende platform blev stående ude i Nordsøen, og fem færdigborede produktionsbrønde, hvoraf de tre står klar til brug.<br>
Da DONG “solgte ud” af deres fossile aktiver og rebrandede sig som Ørsted, opkøbte INEOS alle DONGs aktiver (undtagen rørledninger) i Nordsøen, og hermed også licensen til Hejrefeltet. 
I 2021 opkøbte INEOS desuden Hess Corporation, som var operatør på nabofeltet Syd Arne. Da INEOS nu ejer nabofeltet, kan udbygningen af Hejrefeltet gøres simplere end DONGs oprindelige projekt. 
De nye planer indebærer opførelsen af en ubemandet platform, der fjernstyres fra Syd Arne, samt et strømkabel og en 30km rørledning, der skal transportere strøm til Hejre, og føre gas og olie fra Hejre til Syd Arne. 
Her bliver olie og gas behandlet, før olien opbevares i en tank på havbunden, før den transporteres i tankskibe til Esbjerg Havn og gassen transporteres i en rørledning til Nybro.
INEOS sendte en ansøgning med deres planer for Hejre til Energistyrelsen d. 23. december 2022. Projektet miljøkonsekvensrapport, som er udarbejdet af COWI, blev sendt i offentlig høring med deadline 1. september 2023. 
13. oktober 2023 meddeler Energistyrelsen 7 års forlængelse af licenserne i Hejrefeltet til INEOS, mens selve godkendelsen af projektet lader vente på sig.
